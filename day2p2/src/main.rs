use std::collections::HashMap;

fn main() {
    let your_play = HashMap::from([
        ('Y', 0),
        ('Z', 1),
        ('X', 2)
    ]);

    let opponent_play = HashMap::from([
        ('A', 0),
        ('B', 1),
        ('C', 2)
    ]);

    let points_for_win = HashMap::from([
        (0, 3),
        (1, 6),
        (2, 0)
    ]);

    let x = input_string();
    let vec: Vec<char> = x.chars().collect();
    let mut round_data = [0,0];
    let mut score = 0;
    for c in vec {
        if c == 0x20 as char {
            // ignore space chars
            continue;
        } else if c == 0xA as char {
            // new line means round over
            let mut new_index = round_data[0] + round_data[1];
            new_index %= 3;
            score += points_for_win[&round_data[1]] + new_index + 1;
        } else {
            if opponent_play.contains_key(&c) {
                round_data[0] = opponent_play[&c];
            } else if your_play.contains_key(&c) {
                round_data[1] = your_play[&c];
            }
        }
    };
    let mut new_index = round_data[0] + round_data[1];
            new_index %= 3;
            score += points_for_win[&round_data[1]] + new_index + 1;
    println!("{}", score)
}


fn main() {
    let mut init_setup = vec![
        vec!['Z', 'P', 'M', 'H', 'R'],
        vec!['P', 'C', 'J', 'B'],
        vec!['S', 'N', 'H', 'G', 'L', 'C', 'D'],
        vec!['F', 'T', 'M', 'D', 'Q', 'S', 'R', 'L'],
        vec!['F', 'S', 'P', 'Q', 'B', 'T', 'Z', 'M'],
        vec!['T', 'F', 'S', 'Z', 'B', 'G'],
        vec!['N', 'R', 'V'],
        vec!['P', 'G', 'L', 'T', 'D', 'V', 'C', 'M'],
        vec!['W', 'Q', 'N', 'J', 'F', 'M', 'L']
    ];
    let x = input_string();
    let moves = x.lines();
    for step in moves {
        let clean_step = &step.trim();
        let info = &clean_step.replace("move ", "")
            .replace(" from", "")
            .replace(" to", "");
        let array_info = &info
            .split(' ')
            .map(|num| num.parse::<usize>().unwrap())
            .collect::<Vec<_>>();
        move_stack(&mut init_setup, array_info[1], array_info[2], array_info[0]);
    };
    let mut final_string = "".to_owned();
    for stack in init_setup {
        final_string.push(*stack.last().unwrap());
    }
    println!("{:?}", &final_string)
}

fn move_stack<'a>(setup: &'a mut Vec<Vec<char>>, curr_stack: usize, target_stack: usize, moves: usize) -> &'a mut Vec<Vec<char>> {
    for _n in 0..moves {
        let top = setup[curr_stack-1].last().unwrap().clone();
        setup[target_stack-1].push(top);
        setup[curr_stack-1].pop();
    };
    return setup
}

